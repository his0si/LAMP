"""Phase 3 — Timeloop wrapper (Eyeriss-like 16×16 PE array, PAT energy model).

Generates a single combined Timeloop YAML per (shape, bits_w) and invokes
`timeloop-mapper`. Parses the resulting `timeloop-mapper.stats.txt` into a
`MappingResult`.

Implementation notes:
  * We use Timeloop's built-in PAT (Power Area Timing) model rather than
    Accelergy plug-ins for the first cut — keeps the dependency surface
    smaller and matches what the bundled `configs/mapper/sample.yaml`
    does. Migration to Accelergy is a follow-up (see §3-3 in
    `experiments/README.md`).
  * Matmul (M × K → 1, batch N=1) is expressed as a 1×1 CNN:
      C = K_in, K = M_out, R = S = P = Q = N = 1, strides = 1.
  * Bit-width effect: word-bits in arithmetic + weight-side storage levels
    is set to `bits_w`. AccumulationBuffer remains 24-bit to model the
    integer accumulator. Cycle counts come from the mapper search; bytes
    and pJ-per-byte scale with word-bits via Timeloop's PAT formulas.
  * We invoke the mapper with `timeout` + `</dev/null` so the ncurses TUI
    doesn't hang on non-interactive shells. `victory-condition` + small
    `timeout` keep wall time per run to ~30-90 seconds.

Requirements: `timeloop-mapper` on PATH (see `experiments/scripts/install_timeloop.sh`).
"""
from __future__ import annotations

import re
import shutil
import subprocess
from dataclasses import dataclass
from pathlib import Path

from .config import EXPERIMENTS_ROOT


@dataclass
class LayerWorkload:
    name: str         # e.g. "layer07.q_proj" or just shape role "q_proj"
    M: int            # output features (CNN K)
    N: int            # batch (CNN N)
    K: int            # input features  (CNN C)
    bits_w: int       # weight bit-width
    bits_a: int = 16  # activation bit-width (storage estimate only)


@dataclass
class HWResult:
    layer: str
    bits_w: int
    cycles: int
    energy_pJ: float
    pe_utilization: float
    dram_bytes: int
    gbuf_hit_rate: float
    mapper_seconds: float = 0.0


def _check_tools_available() -> None:
    if not shutil.which("timeloop-mapper"):
        raise RuntimeError(
            "`timeloop-mapper` not on PATH. Run "
            "`bash experiments/scripts/install_timeloop.sh` then re-activate the env."
        )


# ---- YAML generation -------------------------------------------------------

_MAPPER_TEMPLATE = """\
# Auto-generated by pipeline.hw_timeloop. Do not edit by hand.

mapper:
  algorithm: random-pruned
  optimization-metrics:
  - energy
  - delay
  search-size: {search_size}
  victory-condition: {victory}
  timeout: {timeout}
  num-threads: 4
  live-status: False
  log-stats: False

arch:
  arithmetic:
    instances: {pe_total}
    meshX: {mesh_x}
    word-bits: {bits_w}
  storage:
  - name: Registers
    entries: 1
    instances: {pe_total}
    meshX: {mesh_x}
    word-bits: {bits_w}
    cluster-size: {cluster_size}
  - name: AccumulationBuffer
    entries: 128
    instances: {mesh_x}
    meshX: {mesh_x}
    word-bits: 24
    cluster-size: {mesh_x}
    network-word-bits: 16
  - name: WeightInputBuffer
    sizeKB: {gbuf_kb}
    instances: 1
    word-bits: {bits_w}
    block-size: 64
  - name: DRAM
    technology: DRAM
    instances: 1
    word-bits: {bits_w}
    block-size: 64
    bandwidth: {dram_bw_GBps}

mapspace:
  constraints:
  - target: AccumulationBuffer
    type: datatype
    keep: [Outputs]
    bypass: [Weights, Inputs]
  - target: WeightInputBuffer
    type: datatype
    keep: [Weights, Inputs]
    bypass: [Outputs]
  # Spatial unrolling: spread output channels (K) across the X-mesh,
  # input channels (C) across the Y-mesh inside each cluster.
  - target: AccumulationBuffer
    type: spatial
    factors: P1 Q1 R1 S1 C{cluster_size} K1 N1
    permutation: KQRSPNC
  - target: WeightInputBuffer
    type: spatial
    factors: P1 Q1 R1 S1 C1 K{mesh_x} N1
    permutation: KCQRSPN
  - target: Registers
    type: temporal
    factors: R1 S1 C1 K1 N1
    permutation: PQRSCKN
  - target: AccumulationBuffer
    type: temporal
    factors: P1 Q1 R1 S1 C1 N1
    permutation: KPQRSCN
  - target: WeightInputBuffer
    type: temporal
    factors: P1 Q1 K1
  - target: Registers
    type: utilization
    min: 0.01

problem:
  shape: cnn-layer
  R: 1
  S: 1
  P: 1
  Q: 1
  C: {C}
  K: {K_out}
  N: {N}
  Wstride: 1
  Hstride: 1
  Wdilation: 1
  Hdilation: 1
"""


def _materialize_arch(workload: LayerWorkload, run_dir: Path, *,
                      mesh_x: int = 16, cluster_size: int = 16,
                      gbuf_kb: int = 256, dram_bw_GBps: float = 32.0,
                      mapper_timeout_s: int = 60, victory: int = 10) -> Path:
    """Write a combined Timeloop input YAML and return the path."""
    run_dir.mkdir(parents=True, exist_ok=True)
    yaml_text = _MAPPER_TEMPLATE.format(
        bits_w=workload.bits_w,
        pe_total=mesh_x * cluster_size,
        mesh_x=mesh_x,
        cluster_size=cluster_size,
        gbuf_kb=gbuf_kb,
        dram_bw_GBps=dram_bw_GBps,
        C=workload.K,
        K_out=workload.M,
        N=workload.N,
        search_size=0,
        victory=victory,
        timeout=mapper_timeout_s,
    )
    yaml_path = run_dir / "mapper.yaml"
    yaml_path.write_text(yaml_text)
    return yaml_path


# ---- stats.txt parsing -----------------------------------------------------

def parse_stats(stats_path: Path | str, bits_w: int = 8) -> dict:
    """Parse Timeloop's `timeloop-mapper.stats.txt`.

    Returns `{cycles, energy_pJ, pe_utilization, dram_bytes, gbuf_hit_rate}`.
    Cycles/Energy/Utilization come from the headline summary at the bottom
    of the file (not the arithmetic-only Level-0 stats at the top).
    """
    text = Path(stats_path).read_text()

    # Take the FINAL summary block (after the per-level stats) which prints
    # the headline `Utilization:`, `Cycles:`, `Energy:` lines.
    tail = "\n".join(text.splitlines()[-60:])

    def _get(pattern: str, src: str, scale: float = 1.0) -> float:
        m = re.search(pattern, src, re.M)
        return float(m.group(1)) * scale if m else 0.0

    out = {
        "cycles":         _get(r"^Cycles:\s*([0-9.eE+]+)", tail),
        "energy_pJ":      _get(r"^Energy:\s*([0-9.eE+\-]+)\s*uJ", tail, scale=1e6),
        "pe_utilization": _get(r"^Utilization:\s*([0-9.]+)", tail),
    }

    # DRAM bytes: every `=== DRAM ===` section has "Total scalar accesses"
    # for each tensor (Weights/Inputs/Outputs). They share the arch's DRAM
    # word-bits, which equals `bits_w` since we set them together.
    dram_accesses = 0
    in_dram = False
    for line in text.splitlines():
        s = line.strip()
        if s == "=== DRAM ===":
            in_dram = True
            continue
        if in_dram:
            if s.startswith("===") and s != "=== DRAM ===":
                in_dram = False
                continue
            m = re.match(r"Total scalar accesses\s*:\s*(\d+)", s)
            if m:
                dram_accesses += int(m.group(1))
    out["dram_bytes"] = dram_accesses * bits_w // 8

    # GBuf hit-rate proxy not derived in v1 — leave 0 for now.
    out["gbuf_hit_rate"] = 0.0
    return out


# ---- main entry ------------------------------------------------------------


def run_layer(
    workload: LayerWorkload,
    *,
    run_root: Path | str = EXPERIMENTS_ROOT / "results" / "hw",
    mapper_timeout_s: int = 60,
    wall_timeout_s: int = 180,
) -> HWResult:
    """Run timeloop-mapper for one (shape, bits) and parse stats."""
    _check_tools_available()
    run_dir = Path(run_root) / "runs" / f"{workload.name}_w{workload.bits_w}"
    _materialize_arch(workload, run_dir, mapper_timeout_s=mapper_timeout_s)

    import time
    t0 = time.time()
    proc = subprocess.run(
        ["timeout", str(wall_timeout_s), "timeloop-mapper", "mapper.yaml"],
        cwd=run_dir, stdin=subprocess.DEVNULL,
        stdout=subprocess.PIPE, stderr=subprocess.STDOUT, text=True,
    )
    elapsed = time.time() - t0

    stats = run_dir / "timeloop-mapper.stats.txt"
    if not stats.exists():
        log = run_dir / "mapper.stdout.txt"
        log.write_text(proc.stdout or "")
        raise RuntimeError(
            f"timeloop-mapper produced no stats.txt for {workload.name}@{workload.bits_w}b "
            f"(rc={proc.returncode}). See {log}"
        )

    parsed = parse_stats(stats, bits_w=workload.bits_w)
    return HWResult(
        layer=workload.name,
        bits_w=workload.bits_w,
        cycles=int(parsed["cycles"]),
        energy_pJ=parsed["energy_pJ"],
        pe_utilization=parsed["pe_utilization"],
        dram_bytes=int(parsed["dram_bytes"]),
        gbuf_hit_rate=parsed["gbuf_hit_rate"],
        mapper_seconds=elapsed,
    )


def layer_workloads_from_model(target_cfg: dict) -> list[LayerWorkload]:
    """Enumerate the seven linear layers per decoder block.

    Order matches HF Qwen2/Llama: q_proj, k_proj, v_proj, o_proj,
    gate_proj, up_proj, down_proj.
    """
    h = target_cfg["hidden_size"]
    inter = target_cfg["intermediate_size"]
    nh, kv = target_cfg["num_attention_heads"], target_cfg["num_kv_heads"]
    head_dim = h // nh
    out: list[LayerWorkload] = []
    for l in range(target_cfg["num_hidden_layers"]):
        out.extend([
            LayerWorkload(f"layer{l:02d}.q_proj",    M=nh * head_dim, N=1, K=h, bits_w=16),
            LayerWorkload(f"layer{l:02d}.k_proj",    M=kv * head_dim, N=1, K=h, bits_w=16),
            LayerWorkload(f"layer{l:02d}.v_proj",    M=kv * head_dim, N=1, K=h, bits_w=16),
            LayerWorkload(f"layer{l:02d}.o_proj",    M=h,             N=1, K=nh * head_dim, bits_w=16),
            LayerWorkload(f"layer{l:02d}.gate_proj", M=inter,         N=1, K=h, bits_w=16),
            LayerWorkload(f"layer{l:02d}.up_proj",   M=inter,         N=1, K=h, bits_w=16),
            LayerWorkload(f"layer{l:02d}.down_proj", M=h,             N=1, K=inter, bits_w=16),
        ])
    return out
